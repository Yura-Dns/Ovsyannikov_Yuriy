package ru.job4j.calculate;
/**
 * (Определение максимального значения)
 */
public class FindMaxValueOfThree {
    int maxOfTwo;
    public int findMaxValueTwo(int first, int second) {
        return maxOfTwo = first > second ? first : second;
    }
    public int findMaxValueThree(int maxOfTwo, int three){
        return maxOfTwo > three ? maxOfTwo : three;
    }
}